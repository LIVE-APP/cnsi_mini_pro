CREATE TABLE User_booking (
OrderId INT GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1000 INCREMENT BY 1,
Name varchar(20),
mobile number(10),
email varchar(20),
source varchar(20),
destination varchar(20),
from_address varchar(60),
to_address varchar(60),
ordercode number(20),
reg_date  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
);